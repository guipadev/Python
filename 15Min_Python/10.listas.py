# listas
# Nos permite guardar varias variables

personas = ["Nataly", "Lucila", "Erika"]

# muestra todos los datos 
print("muestra lista personas: " + str(personas))

# mostrar una variable especifica
print("muestra especificamente a: " + personas[1])

# mostrar todoa la lista ordenada con un ciclo for
for persona in personas:
    print("-" + persona)    # Para hasta sacar todas las personas