'''
Escribe una función llamada longitud que reciba una lista y retorne la longitud de la lista:

# escribe la función longitud acá

# código de prueba
print(longitud([1, 2])) # 2
print(longitud(['a', 'b', 'c', 'd', 'e'])) # 5
print(longitud([])) # 0
'''

def longitud(lista):

    return len(lista)



print(longitud([1, 2])) # 2
print(longitud(['a', 'b', 'c', 'd', 'e'])) # 5
print(longitud([])) # 0